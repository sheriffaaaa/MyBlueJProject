public class CoffeDrink
{
  private String name;
  private double price;
  
  //constructor
  public CoffeDrink ( String name, double price )
  {
      this.name = name;
      this.price= price;
  }
  
  //getter
  public String getName (){return name;}
  public double getPrice () {return price ;}
  
 
}
